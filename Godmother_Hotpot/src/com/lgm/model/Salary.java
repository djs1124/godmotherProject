package com.lgm.model;

public class Salary {
	private int sId;
	private int sSalary;
	private User user;
	public int getsId() {
		return sId;
	}
	public void setsId(int sId) {
		this.sId = sId;
	}
	public int getsSalary() {
		return sSalary;
	}
	public void setsSalary(int sSalary) {
		this.sSalary = sSalary;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	
	
}
