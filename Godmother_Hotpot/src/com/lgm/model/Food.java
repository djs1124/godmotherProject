package com.lgm.model;

public class Food {
	private int fId; //菜品id
	private String fPic; //菜品图片
	private String fName; //菜品名字
	private String fType; //菜品类型
	private float fUnitPrice; //菜品单价
	private float fSellPrice; //菜品售价
	private int fStock; //菜品库存
	private int fStatic; //菜品状态
	public int getfId() {
		return fId;
	}
	public void setfId(int fId) {
		this.fId = fId;
	}
	public String getfPic() {
		return fPic;
	}
	public void setfPic(String fPic) {
		this.fPic = fPic;
	}
	public String getfName() {
		return fName;
	}
	public void setfName(String fName) {
		this.fName = fName;
	}
	public String getfType() {
		return fType;
	}
	public void setfType(String fType) {
		this.fType = fType;
	}
	public float getfUnitPrice() {
		return fUnitPrice;
	}
	public void setfUnitPrice(float fUnitPrice) {
		this.fUnitPrice = fUnitPrice;
	}
	public float getfSellPrice() {
		return fSellPrice;
	}
	public void setfSellPrice(float fSellPrice) {
		this.fSellPrice = fSellPrice;
	}
	public int getfStock() {
		return fStock;
	}
	public void setfStock(int fStock) {
		this.fStock = fStock;
	}
	public int getfStatic() {
		return fStatic;
	}
	public void setfStatic(int fStatic) {
		this.fStatic = fStatic;
	}
	
	
}
